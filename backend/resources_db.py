def get_curated_resources(goal: str, subjects: str, level: str = "Beginner") -> list:
    """
    Get curated resources based on subjects mentioned in the goal.
    Falls back to verified resources if LLM generation fails.
    """
    import json
    
    resources = []
    subjects_list = [s.strip() for s in subjects.split(',') if s.strip()] if subjects else []
    
    # Get resources for each subject
    for subject in subjects_list:
        # Try exact match first
        if subject in VERIFIED_RESOURCES:
            resources.extend(VERIFIED_RESOURCES[subject])
        else:
            # Try partial match
            for db_subject, db_resources in VERIFIED_RESOURCES.items():
                if subject.lower() in db_subject.lower() or db_subject.lower() in subject.lower():
                    resources.extend(db_resources)
                    break
    
    # If no resources found or insufficient resources, add some general resources
    if len(resources) < 6:
        # Add general resources from multiple subjects
        for subject_resources in VERIFIED_RESOURCES.values():
            resources.extend(subject_resources)
            if len(resources) >= 12:
                break
    
    # Limit and deduplicate
    seen_urls = set()
    unique_resources = []
    for resource in resources:
        if resource['url'] not in seen_urls:
            seen_urls.add(resource['url'])
            unique_resources.append(resource)
    
    return unique_resources[:12]  # Return up to 12 resources
